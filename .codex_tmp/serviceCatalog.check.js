  if (legacyStandaloneLanding) {
    return legacyStandaloneLanding;
  }
  const resolvedId = resolveServiceId(serviceId);
  if (!resolvedId) {
    return null;
  }
  const service = SERVICE_CATALOG[resolvedId];
